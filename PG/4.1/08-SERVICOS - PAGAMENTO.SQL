THEN  
/*IPM-atende.net*/
--1. À vista
--2. A prazo
--3. Depósito
--4. Na apresentação
--5. Cartão de débito
--6. Cartão de crédito
--7. Cheque
--8. PIX
--9. Boleto
    CASE COALESCE(ADIANTAMENTO.TIPOPGTO, PDVAPGTORAZAO.TIPOPGTO, PGTORAZAO.TIPOPGTO, RAZOES.TIPOPGTO)
        WHEN 1 THEN 1
        WHEN 2 THEN 7
        WHEN 3 THEN 5
        WHEN 4 THEN CASE  
                    WHEN CADEMPRESA.NOMEEMPRESA IN ('PIX') THEN 8
                    WHEN CADEMPRESA.NOMEEMPRESA LIKE 'PIX %' THEN 8
                    WHEN (' '|| CADEMPRESA.NOMEEMPRESA||' ') ILIKE '% PIX %' THEN 8
                    WHEN CADEMPRESA.tipocartao = 1 THEN 6
                    ELSE 5 END
        WHEN 5 THEN 2 --CASE WHEN duplicatas.numero IS NULL THEN 2 ELSE 9 END
        -- WHEN 6 THEN 0
        -- WHEN 7 THEN 0
        WHEN 8 THEN 1
        WHEN 10 THEN 4
        WHEN 13 THEN 3
        WHEN 17 THEN 1
    END 
