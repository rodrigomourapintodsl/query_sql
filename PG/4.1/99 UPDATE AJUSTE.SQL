/*update ibpt*/
UPDATE DETNOTAFISCAL SET 
 PCALIQIBPTMUN = IBPTITEM.PCALIQMUN
,VLIBPTMUN     = round(IBPTITEM.PCALIQMUN *DETNOTAFISCAL.VALTOTAL)/100
,PCALIQIBPTEST = IBPTITEM.PCALIQEST
,VLIBPTEST     = round(IBPTITEM.PCALIQEST *DETNOTAFISCAL.VALTOTAL)/100
,pcaliqibpt    = IBPTITEM.PCALIQNAC
,VLIBPT        = round(IBPTITEM.PCALIQNAC *DETNOTAFISCAL.VALTOTAL)/100
  FROM DETNOTAFISCAL AS D
  JOIN NOTASFISCAIS ON D.CODHOTEL = NOTASFISCAIS.CODHOTEL AND D.CODNOTA = NOTASFISCAIS.CODNOTA
  JOIN NFERETORNO ON NOTASFISCAIS.CODNOTA = NFERETORNO.CDNOTA AND NOTASFISCAIS.CODHOTEL = NFERETORNO.CDEMPRESA
  JOIN CADPRODUTO ON CADPRODUTO.CODHOTEL = D.CODHOTEL 
   AND CADPRODUTO.CODPRODUTO = COALESCE(D.CODPRODAGRUPADOR,D.CODPRODCADASTRO,D.CODPRODUTO)
  JOIN CADNBS ON CADNBS.CODNBS = CADPRODUTO.CODNBS AND CADNBS.CODHOTEL = CADPRODUTO.CODHOTEL 
  JOIN IBPTITEM 
    ON IBPTITEM.FLTABELA = 1
    AND IBPTITEM.IDIBPT = RIGHT('000000000'||CADNBS.IDNBS,9)
    AND IBPTITEM.CODHOTEL = CADPRODUTO.CODHOTEL
 WHERE D.CODHOTEL  = DETNOTAFISCAL.CODHOTEL 
  AND D.CODNOTA    = DETNOTAFISCAL.CODNOTA
  AND D.DETCODNOTA = DETNOTAFISCAL.DETCODNOTA
  AND NFERETORNO.FLTIPONOTAES = 0 
  AND NOTASFISCAIS.DATAEMISSAO >='20260101'
  AND NFERETORNO.FLTIPONOTA = 1 
  AND NFERETORNO.FLSTATUS IN (0,1,3,6,7)
  AND NFERETORNO.IDCHAVE = ''
AND DETNOTAFISCAL.pcaliqibpt    = 0
AND DETNOTAFISCAL.pcaliqibptest = 0
AND DETNOTAFISCAL.pcaliqibptmun = 0
;
UPDATE NOTASFISCAIS SET
 PCALIQIBPTMUN = T.PCALIQIBPTMUN
,VLIBPTMUN     = T.VLIBPTMUN
,PCALIQIBPTEST = T.PCALIQIBPTEST
,VLIBPTEST     = T.VLIBPTEST
,pcaliqibpt    = T.pcaliqibpt
,VLIBPT        = T.VLIBPT
FROM (SELECT D.CODHOTEL,D.CODNOTA 
    ,round(SUM(D.VLIBPTMUN) / SUM(D.VALTOTAL) *10000)/100 AS PCALIQIBPTMUN
    ,SUM(D.VLIBPTMUN) AS VLIBPTMUN
    ,round(SUM(D.VLIBPTEST) / SUM(D.VALTOTAL) *10000)/100 AS PCALIQIBPTEST
    ,SUM(D.VLIBPTEST) AS VLIBPTEST
    ,round(SUM(D.VLIBPT)    / SUM(D.VALTOTAL) *10000)/100 AS pcaliqibpt
    ,SUM(D.VLIBPT) AS VLIBPT
FROM DETNOTAFISCAL D
  JOIN NOTASFISCAIS ON D.CODHOTEL = NOTASFISCAIS.CODHOTEL AND D.CODNOTA = NOTASFISCAIS.CODNOTA
  JOIN NFERETORNO ON NOTASFISCAIS.CODNOTA = NFERETORNO.CDNOTA AND NOTASFISCAIS.CODHOTEL = NFERETORNO.CDEMPRESA
WHERE NFERETORNO.FLTIPONOTAES = 0 
  AND NOTASFISCAIS.DATAEMISSAO >='20260101'
  AND NFERETORNO.FLTIPONOTA = 1 
  AND NFERETORNO.FLSTATUS IN (0,1,3,6,7)
  AND NFERETORNO.IDCHAVE = ''
  AND D.pcaliqibpt+D.pcaliqibptest+D.pcaliqibptmun> 0
  AND NOTASFISCAIS.pcaliqibpt    = 0
  AND NOTASFISCAIS.pcaliqibptest = 0
  AND NOTASFISCAIS.pcaliqibptmun = 0
GROUP BY D.CODHOTEL,D.CODNOTA
) AS T
WHERE T.CODHOTEL = NOTASFISCAIS.CODHOTEL AND T.CODNOTA = NOTASFISCAIS.CODNOTA
;





