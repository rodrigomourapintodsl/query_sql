SELECT pg_database.datname,
pg_catalog.pg_size_pretty(pg_database_size(pg_database.datname)) AS pg_database_size,
__data::date AS data_nomeada,
pg_stat_file.modification::date,
pg_stat_file.access::date
--,pg_stat_file.*
FROM pg_catalog.pg_database
LEFT JOIN GENERATE_SERIES(CURRENT_DATE - INTERVAL '24 month',CURRENT_DATE, INTERVAL '1 day' ) AS __data
ON pg_database.datname LIKE '%' || to_char(__data,'DDMMYYYY')
OR pg_database.datname LIKE '%' || to_char(__data,'YYYYMMDD')
OR pg_database.datname LIKE '%' || to_char(__data,'YYYY-MM-DD') 
OR pg_database.datname LIKE '%' || to_char(__data,'DD-MM-YYYY')
OR pg_database.datname LIKE '%' || to_char(__data,'DDMMYY')
CROSS JOIN LATERAL pg_catalog.pg_stat_file('base/' || pg_database.oid || '/PG_VERSION') AS pg_stat_file
WHERE pg_database.datname NOT IN ('postgres','template0','template1')
AND pg_database.datname NOT IN ('-__Passou de 3 meses sera excluido__-','fast_ITC_simples_nacional','fast_ITC_presumido_real')
AND split_part(pg_database.datname,'_',1) NOT IN ('feira','ipdv','implanta45','basezerada4.5')
AND ( 1=0
OR NOT pg_database.datname SIMILAR TO '%[0-9]{2}[\-]{0,1}[0-9]{2}[\-]{0,1}[0-9]{2}[\-]{0,1}[0-9]{0,2}' 
OR pg_stat_file.modification <= current_date - INTERVAL '3 month'
--OR __data <= current_date - INTERVAL '3 month'
OR pg_catalog.pg_database_size(pg_database.datname) < 50000000.0
)
ORDER BY __data, pg_stat_file.modification DESC