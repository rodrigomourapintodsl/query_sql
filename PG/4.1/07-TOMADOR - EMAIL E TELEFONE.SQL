SELECT EMAIL,EMAIL_DESTINATARIO,TELEFONE FROM (
SELECT ROW_NUMBER() OVER(ORDER BY ID) AS IDEMAIL,
    EMAIL,
    EMAIL_DESTINATARIO
FROM (
--remover caracteres especiais
SELECT EMAILS AS EMAILS 
    ,TRIM(
        REGEXP_REPLACE(
            SPLIT_PART(EMAIL, ';', 1),
            '[^abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789\@\.\;\_\-]',
            '',
            'g'
        )
    ) AS EMAIL,STRING_AGG(EMAIL,';') OVER() AS EMAIL_DESTINATARIO,
    ROW_NUMBER() OVER(
        ORDER BY MIN(ID)
    ) AS ID
FROM (
--split nome definido <>
        SELECT ID
            ,COALESCE(
                (REGEXP_MATCH(EMAIL, '<(.*)>'))[1],
                EMAIL
            ) AS EMAIL,
            EMAILS
        FROM (
--adicionar ordem
                SELECT ROW_NUMBER() OVER() AS ID 
                    ,*
                FROM (
--split multiplos emails ;
                        SELECT 
                            EMAIL AS EMAILS,
                            regexp_split_to_table(EMAIL, ';') AS EMAIL
                        FROM (
-- unindo registros
                                SELECT string_agg(EMAIL, ';') AS EMAIL 
                                FROM (
--query normal da tabela de emails
SELECT notasfiscais.EMAIL
FROM notasfiscais
WHERE notasfiscais.codhotel = :CDEMPRESA
AND notasfiscais.codnota = :CDNOTA
                                    ) AS NOTASFISCAIS
                            ) AS CONTATO_EMAIL_UNINDO
                    ) AS CONTATO_EMAIL_MULTIPLOS
            ) AS CONTATO_EMAIL_ORDEM
    ) AS CONTATO_EMAIL_NOME_DEFINIDO
WHERE CHAR_LENGTH(EMAIL) >= 10
    AND EMAIL LIKE '%@%.%'
    AND TRIM(SPLIT_PART(EMAIL, ';', 1)) NOT LIKE '% %'
    AND EMAIL NOT LIKE '%<%'
    AND EMAIL NOT LIKE '%>%'
    AND EMAIL NOT LIKE '%,%'
    AND EMAIL NOT LIKE '%"%'
    AND EMAIL NOT LIKE '%''%'
    AND EMAIL NOT LIKE '%@%@%'
    AND EMAIL NOT LIKE '%@.%'
    AND EMAIL NOT LIKE '%@.%'
    AND EMAIL NOT LIKE '%@'
    AND EMAIL NOT LIKE '@%'
    AND EMAIL NOT LIKE '%..%'
    AND EMAIL NOT LIKE '%.'
    AND EMAIL NOT LIKE '.%'
    AND EMAIL NOT LIKE 'naotem@%'
    AND EMAIL NOT LIKE 'sememail@%'
    AND EMAIL NOT LIKE 'sem@%'
    AND EMAIL NOT LIKE 'naoinfo@%'
    AND EMAIL NOT LIKE 'naoinformado@%'
    AND EMAIL NOT LIKE 'naoinformou@%'
    AND EMAIL NOT LIKE 'nao@%'
    AND LEFT(TRIM(EMAIL),-1) NOT LIKE '%;%'
    AND EMAIL NOT SIMILAR TO '%[ŠŽšžŸÀÁÂÃÄÅÇÈÉÊËÌÍÎÏÐÑÒÓÔÕÖÙÚÛÜÝàáâãäåçèéêëìíîïðñòóôõöùúûüýÿ]%'
GROUP BY EMAIL,
    EMAILS
) AS T
ORDER BY ID
) AS E
FULL OUTER JOIN (
SELECT ROW_NUMBER() OVER() AS IDFONE,
   CASE 
       WHEN NOTASFISCAIS.FONEFAX LIKE '0800%'
       THEN NULL
       WHEN LENGTH(REPLACE(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g'),LEFT(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g'),1),'')) < 2
       THEN NULL
       WHEN LENGTH(NOTASFISCAIS.FONEFAX) >= 8 AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) <= 13 AND NOTASFISCAIS.FONEFAX LIKE '+55%'  AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) > 11 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(NOTASFISCAIS.FONEFAX,-3),'[^0-9]','','g')
       WHEN LENGTH(NOTASFISCAIS.FONEFAX) >= 8 AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) <= 13 AND NOTASFISCAIS.FONEFAX LIKE '+ 55%' AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) > 11 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(NOTASFISCAIS.FONEFAX,-4),'[^0-9]','','g')
       WHEN LENGTH(NOTASFISCAIS.FONEFAX) >= 8 AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) <= 13 AND NOTASFISCAIS.FONEFAX LIKE '55%'   AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) > 11 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(NOTASFISCAIS.FONEFAX,-2),'[^0-9]','','g')
       WHEN LENGTH(NOTASFISCAIS.FONEFAX) >= 8 AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) <= 13 AND NOTASFISCAIS.FONEFAX SIMILAR TO '[\(]{0,1}0[1-9]%' AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) = 12 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(NOTASFISCAIS.FONEFAX,-1),'[^0-9]','','g')
       WHEN LENGTH(NOTASFISCAIS.FONEFAX) >= 8 AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) <= 11 AND NOTASFISCAIS.FONEFAX NOT LIKE '+%' AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')
       ----
       WHEN LENGTH(REPLACE(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g'),LEFT(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g'),1),'')) < 2
       THEN NULL
       WHEN LENGTH(CADHOSPEDE.CELULAR) >= 9 AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) <= 13 AND CADHOSPEDE.CELULAR LIKE '+55%' AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) > 11 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(CADHOSPEDE.CELULAR,-3),'[^0-9]','','g')
       WHEN LENGTH(CADHOSPEDE.CELULAR) >= 9 AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) <= 13 AND CADHOSPEDE.CELULAR LIKE '+ 55%' AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) > 11 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(CADHOSPEDE.CELULAR,-4),'[^0-9]','','g')
       WHEN LENGTH(CADHOSPEDE.CELULAR) >= 9 AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) <= 13 AND CADHOSPEDE.CELULAR LIKE '55%' AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) > 11 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(CADHOSPEDE.CELULAR,-2),'[^0-9]','','g')
       WHEN LENGTH(CADHOSPEDE.CELULAR) >= 9 AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) <= 13 AND CADHOSPEDE.CELULAR SIMILAR to '[\(]{0,1}0[1-9]%' AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) = 12 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(RIGHT(CADHOSPEDE.CELULAR,-1),'[^0-9]','','g')
       WHEN LENGTH(CADHOSPEDE.CELULAR) >= 9 AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) <= 11 AND CADHOSPEDE.CELULAR NOT LIKE '+%' AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')
       ----ESTRANGEIRO
       WHEN LENGTH(NOTASFISCAIS.FONEFAX) >= 8 AND LENGTH(REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')) <= 13 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(NOTASFISCAIS.FONEFAX,'[^0-9]','','g')
       WHEN LENGTH(CADHOSPEDE.CELULAR) >= 9 AND LENGTH(REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')) <= 13 AND NOTASFISCAIS.CODPAIS IN ('01058','1058')
       THEN REGEXP_REPLACE(CADHOSPEDE.CELULAR,'[^0-9]','','g')
   END  AS telefone
FROM notasfiscais
left join razoes on notasfiscais.codrazao = razoes.codrazao and notasfiscais.codhotel = razoes.codhotel
left join hospedes on razoes.acesso=hospedes.acesso and hospedes.tipoacesso='E' AND hospedes.titular = 'S' AND razoes.codhotel=hospedes.codhotel
left join cadhospede on cadhospede.codhospede=hospedes.codhospede AND cadhospede.codhotel=hospedes.codhotel
WHERE notasfiscais.codhotel = :CDEMPRESA
AND notasfiscais.codnota = :CDNOTA
  AND (LENGTH(notasfiscais.fonefax) BETWEEN 8 AND 16 
    OR LENGTH(notasfiscais.fonefax) BETWEEN 8 AND 20 AND notasfiscais.fonefax LIKE '+%'
    OR LENGTH(cadhospede.celular  ) BETWEEN 8 AND 16
    OR LENGTH(cadhospede.celular  ) BETWEEN 8 AND 20 AND cadhospede.celular LIKE '+%'
    )
) AS F ON IDFONE=IDEMAIL
WHERE IDFONE IS NOT NULL OR IDEMAIL IS NOT NULL 
ORDER BY IDEMAIL