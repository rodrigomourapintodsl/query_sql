    || TRIM(TO_CHAR(DOCFISCAL.DTENTRADASAIDA,'YYDD'))
    || TRIM(TO_CHAR(DOCFISCAL.CDDOCFISCAL,'000000000'))
    AS ID ) AS NFS
    )
