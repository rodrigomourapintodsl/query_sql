SELECT 
    DOCFISCAL.CDDOCFISCAL AS CODNOTA,
    DOCFISCAL.CDDOCFISCAL AS CDNOTA,
    DOCFISCAL.CDEMPRESA,
    DOCFISCAL.NRDOCFISCAL AS NRONOTA,
    COALESCE(NULLIF(NULLIF(CAST(regexp_replace(DOCFISCAL.IDSERIE, '[^0-9]', '', 'g') AS VARCHAR(5)),'0'),''),'1') AS SERIE
FROM DOCFISCAL
WHERE DOCFISCAL.CDEMPRESA = :CDEMPRESA
AND DOCFISCAL.CDDOCFISCAL = :CDNOTA